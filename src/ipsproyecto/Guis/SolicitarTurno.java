    private void btnGenerarTurnoActionPerformed(java.awt.event.ActionEvent evt) {
        GestionTurnos gestionTurnos = new GestionTurnos();

        if (pacienteGlobal == null) {
            JOptionPane.showMessageDialog(null, "No se ha seleccionado un paciente");
            return;
        }

        gestionTurnos.agregarPaciente(pacienteGlobal);
        String turno = gestionTurnos.obtenerTurno(pacienteGlobal.getCedula());
        txtTurno.setText(turno);
        JOptionPane.showMessageDialog(null, "Turno generado con exito");
        JOptionPane.showMessageDialog(null, "Turno: " + turno);
    }

