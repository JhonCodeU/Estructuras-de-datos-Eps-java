    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnBuscarActionPerformed
        // obtener el valor de la cedula
        String cedula = txtSearch.getText();
        // buscar el paciente
        RegistroPacientes instancia = RegistroPacientes.getInstancia();
        ArrayList<Paciente> pacientes = instancia.obtenerListaPacientes();

        // recorrer la lista de pacientes y retornar el paciente que coincida
        if (pacientes.size() < 1) {
            JOptionPane.showMessageDialog(null, "No hay pacientes registrados");
        }
        for (Paciente paciente : pacientes) {
            if (paciente.getCedula().equals(cedula)) {
                System.out.println(paciente.getNombre());
                // si el paciente existe, abrir la ventana de solicitar turno
                // Mensaje de que se ha encontrado el paciente
                JOptionPane.showMessageDialog(null, "Paciente encontrado");
                // setear nombre en txtNombre
                jLabel4.setText(paciente.getNombre() + " " + paciente.getApellidos());
                pacienteGlobal = paciente;
                System.out.println(pacienteGlobal.getNombre());
                break;
            }
        }

    }// GEN-LAST:event_btnBuscarActionPerformed

